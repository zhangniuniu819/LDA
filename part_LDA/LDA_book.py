def run_lda_books(df, text_col, num_topics, passes):
    print("开始训练书籍评论 LDA（女性主义聚焦版）...", flush=True)
    outfile.write(f"\n{'='*60}\n书籍评论 LDA 主题分析（白名单模式）\n{'='*60}\n")
    outfile.write(f"白名单仅保留 {len(feminist_core_words)} 个核心词\n\n")

    texts = df[text_col].apply(tokenize_book).tolist()
    texts = [t for t in texts if len(t) >= 2]
    outfile.write(f"有效文档数（含至少2个核心词）: {len(texts)}\n")
    print(f"有效文档数: {len(texts)}")

    if len(texts) < 50:
        outfile.write("警告：有效文档数过少，请检查数据或放宽过滤条件。\n")

    dictionary = corpora.Dictionary(texts)
    dictionary.filter_extremes(no_below=NO_BELOW, no_above=NO_ABOVE)
    dictionary.compactify()
    corpus = [dictionary.doc2bow(text) for text in texts]

    lda_model = models.LdaModel(
        corpus, id2word=dictionary,
        num_topics=num_topics,
        passes=passes,
        iterations=ITERATIONS,
        random_state=42,
        eval_every=None,
        alpha='auto',
        eta='auto'
    )

    outfile.write("\n各主题 top-15 关键词:\n")
